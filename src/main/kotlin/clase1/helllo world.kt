package clase1

fun main (){
    println(" hola mundo")

    /**
     * variables:
     * var: se representan variables que se pueden cambiar.
     * val: no se pueden cambiar
     */

    val size =10 // Int / Integer
    println(size)

    var temperature= 30.5 // float
    temperature = 36.0
    temperature = 37.0
    temperature= 38.0
    temperature=40.0
    println(temperature)

    var turn_on=true // boolean
    println(turn_on)


}